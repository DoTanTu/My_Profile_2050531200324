package com.example.my_android_project;

public class Car {

    String name, lastMessage, lastMsgTime, phoneNo, country;
    int imageId;

    public Car(String name, String lastMessage, String lastMsgTime, String phoneNo, String country, int imageId) {
        this.name = name;
        this.lastMessage = lastMessage;
        this.lastMsgTime = lastMsgTime;
        this.phoneNo = phoneNo;
        this.country = country;
        this.imageId = imageId;
    }
}
